'''請使用選擇敘述撰寫一程式,讓使用者輸入一個整數
   然後判斷它是否為 2 或 7 的倍數'''

x=eval(input('請輸入整數:'))
if (((x%2)==0) and ((x%7)==0)):
    print(x,'是2及7的倍數',sep='')
elif ((x%2)==0):
    print(x,'是2的倍數',sep='')
elif ((x%7)==0):
    print(x,'是7的倍數',sep='') 
else:
    print(x,'不是2也不是7的倍數',sep='')
    
    
    
        